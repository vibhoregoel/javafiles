class increment{
	            public static void main(String[] args)
	            {
	             int x = 2;
	             int y = x++;
	            
	             
	             System.out.println(x);
	             System.out.println(y);

	             int a = 3;
	             int z = ++a;
	             System.out.println(a);
	             System.out.println(z);

	             
	            }
}